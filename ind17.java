public class Ind16{
public static void main(String args[])
{
System.out.println("Display arithmetic operations with mixed data type:");
System.out.println("-------------------------------------------------------");

int a=5 ,b=7;
double c=3.7,d=8.0;

System.out.println("5+7= " + (a+b));
System.out.println("3.7+8.0= "+(c+d));
System.out.println("5+8.0= "+(a+d));
System.out.println("5-7= "+(a-b));
System.out.println("3.7-8.0="+(c-d));
System.out.println("5-8.0="+(a-d));
System.out.println("5*7="+(a*b));
System.out.println("3.7*8.0="+(c*d));
System.out.println("5*8.0="+(a*d));
System.out.println("5/7="+(a/b));
System.out.println("3.7/8.0="+(c/d));
System.out.println("5/8.0="+(a/d));

}
}